const soma = (number1, number2) => {
  return number1 + number2
}

const sub = (number1, number2) => {
  return number1 - number2
}

const mult = (number1, number2) => {
  return number1 * number2
}

const div = (number1, number2) => {
  return number1 / number2
}

console.log(soma(2, 5))
console.log(sub(2, 5))
console.log(mult(2, 5))
console.log(div(2, 5))